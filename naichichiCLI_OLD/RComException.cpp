//エラーを文字列かして先頭に加えます。
string RComException::AppendComError(HRESULT inErrorCode)
{
	LPVOID msgBuf;
	::FormatMessage( 
		FORMAT_MESSAGE_ALLOCATE_BUFFER | 
		FORMAT_MESSAGE_FROM_SYSTEM | 
		FORMAT_MESSAGE_IGNORE_INSERTS,
		NULL,
		inErrorCode,
		MAKELANGID(LANG_NEUTRAL, SUBLANG_DEFAULT), // デフォルト言語
		(LPTSTR) &msgBuf,
		0,
		NULL 
	);

	if (msgBuf == NULL) return "";
	string err = (char*)msgBuf;

	::LocalFree( msgBuf );
	return err;
}

//COMの例外を捕まえてみる.
string RComException::AppendComErrorEx(IUnknown * inUnkown , REFIID inRIID)
{
	USES_CONVERSION;

	if (inUnkown == NULL) return "";

	string ret;

	ISupportErrorInfo *pSupport;
	HRESULT hr = inUnkown->QueryInterface(IID_ISupportErrorInfo, (void**)&pSupport);
	if (FAILED(hr)) return "";

	hr = pSupport->InterfaceSupportsErrorInfo(inRIID);
	if (hr == S_OK) 
	{ // can't use SUCCEEDED here! S_FALSE succeeds!
		IErrorInfo *pErrorInfo;
		hr = GetErrorInfo(0, &pErrorInfo);
		if (SUCCEEDED(hr)) 
		{
			BSTR desc = 0 , src= 0;
			hr = pErrorInfo->GetDescription(&desc);
			ASSERT(SUCCEEDED(hr));
			hr = pErrorInfo->GetSource(&src);
			ASSERT(SUCCEEDED(hr));

			ret = string("") + 
			"Description:　" + W2A(desc) + "\n" + 
			"Source:　" + W2A(src) + "\n" ;

			// FINALLY can call methods on pErrorInfo!
			// ...and handle the error!
			pErrorInfo->Release();  // don't forget to release!
		}
	}
	pSupport->Release();

	return ret;
}

